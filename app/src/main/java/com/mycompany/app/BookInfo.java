    public BookInfo(Book[] books) {
        initComponents();
        this.model.addColumn("CODE");
        this.model.addColumn("TITLE");
        this.model.addColumn("AUTHOR");
        this.books = books;
        
        for(Book book : books ) {
            this.model.addRow(new Object[]{book.getCode(), book.getTitle(), book.author});
        }
        
        this.myTable.setModel(this.model);
    }

